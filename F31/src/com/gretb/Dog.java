package com.gretb;

public class Dog {
	
	private int age;//age of Dog
	private String name;//name of Dog
	//constructor
	public Dog(int age, String name) {
		super();
		this.age = age;
		this.name = name;
	}
	@Override
	public String toString() {
		return "Dog [age=" + age + ", name=" + name + "]";
	}
	
	
	
	
	
	
	

}
